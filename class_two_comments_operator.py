#Operators in Python
x = 10  # Assignment operator
y = 5
print("Addition:", x + y)  # Addition operator
print("Subtraction:", x - y)  # Subtraction operator
print("Multiplication:", x * y)  # Multiplication operator
print("Division:", x / y)  # Division operator
print("Modulus:", x % y)  # Modulus operator
print("Exponentiation:", x ** y)  # Exponentiation operator
print("Floor Division:", x // y)  # Floor Division operator
print("Equality:", x == y)  # Equality operator
print("Greater than:", x > y)  # Greater than operator
print("Less than:", x < y)  # Less than operator
print("Logical AND:", x > 5 and y < 10)  # Logical AND
print("Logical OR:", x > 5 or y > 10)  # Logical OR
print("Logical NOT:", not(x > 5))  # Logical NOT
print("Bitwise AND:", x & y)  # Bitwise AND
print("Bitwise OR:", x | y)  # Bitwise OR
print("Bitwise XOR:", x ^ y)  # Bitwise XOR
print("Bitwise NOT:", ~x)  # Bitwise NOT
